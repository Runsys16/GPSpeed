void ScreenNetInfo::print(void)
{
  int x=5, y=8, df, dy;

  tft->setFreeFont(FSS9);
  tft->setTextColor(TFT_DARKGREY);
  tft->setTextDatum(BL_DATUM);

  df = tft->fontHeight();
  y+=df;
  dy = df + df/6;

  tft->drawString( "Connexion Wifi :", x, y );
  
  y += dy+8;
  x = 20;
  tft->drawString( "SSID :", x, y );

  tft->setTextColor(TFT_WHITE);
  tft->setFreeFont(FSSB12);
  tft->drawString( "GPSpeed", x+60, y );
  y += dy;
  tft->drawString( "http://172.16.9.1/", x, y );

  bUpdate = false;
}

